#include <main.h>

/**
 * main - Determine if a number is positive, negative or zero.
 *
 * Return: 0 on success
*/
void positive_or_negative(int i)
{
if (i < 0)
{
printf("%d is %s\n", "negative");
}
else if (i > 0)
{
printf("%d is %s\n", "positive");
}
else
{
printf("%d is %s\n", "negative");
}
return;
}
